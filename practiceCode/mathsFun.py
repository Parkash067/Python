def add (first_number,second_number):
    _sum = first_number + second_number
    return _sum

def diff (first_number,second_number):
    subtract = first_number - second_number
    return subtract

def prod (first_number,second_number):
    _prod = first_number * second_number
    return _prod

def divide (first_number,second_number):
    _div = first_number / second_number
    return _div